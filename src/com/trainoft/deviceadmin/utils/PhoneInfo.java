package com.trainoft.deviceadmin.utils;

import android.content.Context;
import android.os.Build;
import android.provider.Settings.Secure;
import android.telephony.TelephonyManager;

public class PhoneInfo {

	Context c;

	public PhoneInfo(Context ctx) {
		this.c = ctx;
	}

	
////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////

	/*
	 * get Sim serial number  
	 */
	public String getSimID() {
		TelephonyManager tm = (TelephonyManager) c
				.getSystemService(Context.TELEPHONY_SERVICE);
		if (tm != null) {
			return tm.getSimSerialNumber();
		} else {
			return null;
		}

	}

	
////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////

	/*
	 * get unique android Id  
	 */
	public String getAndroidID() {
		return Secure.getString(c.getContentResolver(), Secure.ANDROID_ID);

	}

	
////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////

	/*
	 * get IMEI number 
	 */
	public String getIMEI() {
		TelephonyManager tm = (TelephonyManager) c
				.getSystemService(Context.TELEPHONY_SERVICE);
		if (tm != null) {
			return tm.getDeviceId();
		} else {
			return null;
		}

	}

	
////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////

	/*
	 * get host application package name  
	 */
	public String getPackage(){
		return c.getApplicationContext().getPackageName();
	}

////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////

	/*
	 * get device manufacturer and build info  
	 */
	public String deviceInfo() {

		return Build.MANUFACTURER + "/" + Build.MODEL;
	}

}
